import { cn } from '@/lib/utils'
import { Input as InputPrimitive } from '@base-ui/react/input'
import * as React from 'react'

type InputProps = InputPrimitive.Props &
  React.RefAttributes<HTMLInputElement> & {
    onValueChange?: (value: string) => void
  }

function Input({ className, onValueChange, ...props }: InputProps) {
  return (
    <InputPrimitive
      data-slot="input"
      className={cn(
        'h-8 w-full min-w-0 rounded-lg border border-input bg-background px-2.5 py-1 text-sm transition-[color,box-shadow] outline-none file:inline-flex file:border-0 file:bg-transparent file:text-sm file:font-medium file:text-foreground placeholder:text-muted-foreground focus-visible:border-ring focus-visible:ring-[3px] focus-visible:ring-ring/50 disabled:pointer-events-none disabled:cursor-not-allowed disabled:opacity-50 aria-invalid:border-destructive aria-invalid:ring-[3px] aria-invalid:ring-destructive/20 md:text-xm dark:bg-input/30 dark:aria-invalid:border-destructive/50 dark:aria-invalid:ring-destructive/40',
        className,
      )}
      {...props}
      onChange={(e) => {
        props.onChange?.(e)
        onValueChange?.((e.target as HTMLInputElement).value)
      }}
    />
  )
}

export { Input, type InputProps }
